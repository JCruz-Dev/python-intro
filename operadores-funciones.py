def suma(a, b):
    print('Suma', a + b)


def resta(a, b):
    print('Resta', a - b)


def multiplicar(a, b):
    print('Multiplicacion', a * b)


def dividir(a, b):
    # int permite tomar la parte real de un decimal
    print('Division', int(a / b))


def potenciar(a, b):
    print('Potencia', a ** b)


suma(2, 2)
potenciar(4, 3)
dividir(10, 2)
